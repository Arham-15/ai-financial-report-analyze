# ── Constants ─────────────────────────────────────────────────────────────────
FALLBACKS = {
    "Microsoft": {"cash_flow_operations": 118548}
}

COMPANY_COLORS = {
    "Tesla":     "#E31937",
    "Microsoft": "#00A4EF",
    "Apple":     "#555555",
}
